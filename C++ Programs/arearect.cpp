/* CPP PROGRAM TO CALCULATE AREA OF RECTANGLE */
#include <iostream>
using namespace  std;
int main()
{
	float length,breadth,area;
	cout<<"Enter Length Of Rectanle in CM=";
	cin>>length;
	cout<<"Enter breadth Of Rectanle in CM=";
	cin>>breadth;
	area=length*breadth;
	cout<<"Area Of Rectanle = "<<area<<" sq.cm";
	return 0;
}